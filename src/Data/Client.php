<?php

declare(strict_types=1);

namespace Finvalda\Data;

/**
 * Client (customer/supplier) entity DTO.
 */
final class Client extends Entity
{
    public function __construct(
        public readonly string $code,
        public readonly string $name,
        public readonly ?string $name2 = null,
        public readonly ?string $address = null,
        public readonly ?string $address2 = null,
        public readonly ?string $city = null,
        public readonly ?string $postalCode = null,
        public readonly ?string $country = null,
        public readonly ?string $countryName = null,
        public readonly ?string $phone = null,
        public readonly ?string $fax = null,
        public readonly ?string $email = null,
        public readonly ?string $companyCode = null,
        public readonly ?string $vatCode = null,
        public readonly ?string $vatRateCode = null,
        public readonly ?string $bankCode = null,
        public readonly ?string $bankAccount = null,
        public readonly ?string $correspondentBankAccount = null,
        public readonly ?string $responsiblePerson = null,
        public readonly ?int $paymentDays = null,
        public readonly ?float $debt = null,
        public readonly ?float $currentDebt = null,
        public readonly ?float $creditLimit = null,
        public readonly ?float $taxPercent = null,
        public readonly ?string $paymentTermsCredit = null,
        public readonly ?string $paymentTermsDebit = null,
        public readonly ?float $penaltyCredit = null,
        public readonly ?float $penaltyDebit = null,
        public readonly ?int $debtDaysAllowed = null,
        public readonly ?float $maxOverdueDebt = null,
        public readonly ?string $type = null,
        public readonly ?int $typeCode = null,
        public readonly ?string $tag1 = null,
        public readonly ?string $tag2 = null,
        public readonly ?string $tag3 = null,
        public readonly ?string $object1 = null,
        public readonly ?string $object2 = null,
        public readonly ?string $object3 = null,
        public readonly ?string $object4 = null,
        public readonly ?string $object5 = null,
        public readonly ?string $object6 = null,
        public readonly ?string $notes = null,
        public readonly ?string $suggestedCurrency = null,
        public readonly ?bool $hasSubsidiary = null,
        public readonly ?float $additionalInfo = null,
        public readonly ?bool $isIndividual = null,
        public readonly ?bool $isVatPayer = null,
        public readonly ?bool $sendInvoices = null,
        public readonly ?string $debtAccount = null,
        public readonly ?string $creditAccount = null,
        public readonly ?bool $active = null,
        public readonly ?bool $isNew = null,
        public readonly ?string $createdAt = null,
        public readonly ?string $updatedAt = null,
        array $raw = [],
    ) {
        $this->raw = $raw;
    }

    public static function fromArray(array $data): static
    {
        return new self(
            code: (string) ($data['sKodas'] ?? $data['kodas'] ?? ''),
            name: (string) ($data['sPavadinimas'] ?? $data['pavadinimas'] ?? ''),
            name2: $data['sPavadinimas2'] ?? $data['pavadinimas2'] ?? null,
            address: $data['sAdresas'] ?? $data['ardesas'] ?? $data['adresas'] ?? null,
            address2: $data['sAdresas2'] ?? $data['adresas2'] ?? null,
            city: $data['sMiestas'] ?? $data['miestas'] ?? null,
            postalCode: $data['sPastoKodas'] ?? $data['pasto_kodas'] ?? null,
            country: $data['sValstybeKodas'] ?? $data['sValstybe'] ?? $data['valstybe'] ?? null,
            countryName: $data['sValstybe'] ?? $data['sValstybePav'] ?? $data['valstybe_pav'] ?? null,
            phone: $data['sTelefonas'] ?? $data['telefonas'] ?? null,
            fax: $data['sFaksas'] ?? $data['faksas'] ?? null,
            email: $data['sEMail'] ?? $data['sElPastas'] ?? $data['el_pastas'] ?? null,
            companyCode: $data['sImKodas'] ?? $data['sImonesKodas'] ?? $data['im_kodas'] ?? null,
            vatCode: $data['sPvmMoketojoKod'] ?? $data['sPVMKodas'] ?? $data['pvm_moketojo_kodas'] ?? null,
            vatRateCode: $data['sPvmMokKod'] ?? $data['pvm_mok_kod'] ?? null,
            bankCode: $data['sBankas'] ?? $data['bankas'] ?? null,
            bankAccount: $data['sBankoSaskaita'] ?? $data['sBankoSask'] ?? $data['banko_sask'] ?? null,
            correspondentBankAccount: $data['sKorespSaskBank'] ?? $data['korsp_sask_bank'] ?? null,
            responsiblePerson: $data['sAtsakingasAsmuo'] ?? $data['sAtskaitingasAsmuo'] ?? $data['atskaitingas_asmuo'] ?? null,
            paymentDays: isset($data['nAtsiskaitytaPer']) ? (int) $data['nAtsiskaitytaPer']
                : (isset($data['atsiskaityti_per']) ? (int) $data['atsiskaityti_per'] : null),
            debt: isset($data['dSkola']) ? (float) $data['dSkola']
                : (isset($data['skola']) ? (float) $data['skola'] : null),
            currentDebt: isset($data['dEinamojiSkola']) ? (float) $data['dEinamojiSkola']
                : (isset($data['einamoji_skola']) ? (float) $data['einamoji_skola'] : null),
            creditLimit: isset($data['nKreditas']) ? (float) $data['nKreditas']
                : (isset($data['kreditas']) ? (float) $data['kreditas'] : null),
            taxPercent: isset($data['dMokestis']) ? (float) $data['dMokestis']
                : (isset($data['mokestis']) ? (float) $data['mokestis'] : null),
            paymentTermsCredit: $data['sAtsiskaitymoTerminaiKred'] ?? $data['atsiskaitymo_terminai_kred'] ?? null,
            paymentTermsDebit: $data['sAtsiskaitymoTerminaiDebt'] ?? $data['atsiskaitymo_terminai_debt'] ?? null,
            penaltyCredit: isset($data['dDelspinigiaiKred']) ? (float) $data['dDelspinigiaiKred']
                : (isset($data['delspinigiai_kred']) ? (float) $data['delspinigiai_kred'] : null),
            penaltyDebit: isset($data['dDelspinigiaiDebt']) ? (float) $data['dDelspinigiaiDebt']
                : (isset($data['delspinigiai_debt']) ? (float) $data['delspinigiai_debt'] : null),
            debtDaysAllowed: isset($data['nSkolaGalimaPradelstiDien']) ? (int) $data['nSkolaGalimaPradelstiDien'] : null,
            maxOverdueDebt: isset($data['nMaksPradlestosSkolosSum']) ? (float) $data['nMaksPradlestosSkolosSum'] : null,
            type: $data['sRusis'] ?? $data['rusis'] ?? null,
            typeCode: isset($data['nTipas']) ? (int) $data['nTipas'] : null,
            tag1: $data['sPozymis1'] ?? $data['pozymis_1'] ?? $data['pozymis1'] ?? null,
            tag2: $data['sPozymis2'] ?? $data['pozymis_2'] ?? $data['pozymis2'] ?? null,
            tag3: $data['sPozymis3'] ?? $data['pozymis_3'] ?? $data['pozymis3'] ?? null,
            object1: $data['sObjektas1'] ?? $data['objektas1'] ?? null,
            object2: $data['sObjektas2'] ?? $data['objektas2'] ?? null,
            object3: $data['sObjektas3'] ?? $data['objektas3'] ?? null,
            object4: $data['sObjektas4'] ?? $data['objektas4'] ?? null,
            object5: $data['sObjektas5'] ?? $data['objektas5'] ?? null,
            object6: $data['sObjektas6'] ?? $data['objektas6'] ?? null,
            notes: $data['sPastabos'] ?? $data['pastabos'] ?? null,
            suggestedCurrency: $data['sPasiulomaValiuta'] ?? $data['pasiuloma_valiuta'] ?? null,
            hasSubsidiary: isset($data['sYraKlientoPadalinys']) ? (bool) $data['sYraKlientoPadalinys']
                : (isset($data['yra_padalinys']) ? (bool) $data['yra_padalinys'] : null),
            additionalInfo: isset($data['dPapildomaInf']) ? (float) $data['dPapildomaInf']
                : (isset($data['papildoma_inf']) ? (float) $data['papildoma_inf'] : null),
            isIndividual: isset($data['nFizinisAsmuo']) ? (bool) $data['nFizinisAsmuo'] : null,
            isVatPayer: isset($data['nPvmMokaPirkejas']) ? (bool) $data['nPvmMokaPirkejas'] : null,
            sendInvoices: isset($data['nSiustiFakturas']) ? (bool) $data['nSiustiFakturas'] : null,
            debtAccount: $data['sDebtSask'] ?? null,
            creditAccount: $data['sKredSask'] ?? null,
            active: isset($data['nAktyvus']) ? (bool) $data['nAktyvus']
                : (isset($data['aktyvus']) ? (bool) $data['aktyvus'] : null),
            isNew: isset($data['bNaujas']) ? (bool) $data['bNaujas']
                : (isset($data['naujas']) ? (bool) $data['naujas'] : null),
            createdAt: $data['tKurimoData'] ?? $data['kurimo_data'] ?? null,
            updatedAt: $data['tKoregavimoData'] ?? $data['koregavimo_data'] ?? null,
            raw: $data,
        );
    }

    public function toArray(): array
    {
        return array_filter([
            'sKodas' => $this->code,
            'sPavadinimas' => $this->name,
            'sPavadinimas2' => $this->name2,
            'sAdresas' => $this->address,
            'sAdresas2' => $this->address2,
            'sMiestas' => $this->city,
            'sPastoKodas' => $this->postalCode,
            'sValstybeKodas' => $this->country,
            'sValstybe' => $this->countryName,
            'sTelefonas' => $this->phone,
            'sFaksas' => $this->fax,
            'sEMail' => $this->email,
            'sImKodas' => $this->companyCode,
            'sPvmMoketojoKod' => $this->vatCode,
            'sPvmMokKod' => $this->vatRateCode,
            'sBankas' => $this->bankCode,
            'sBankoSaskaita' => $this->bankAccount,
            'sKorespSaskBank' => $this->correspondentBankAccount,
            'sAtsakingasAsmuo' => $this->responsiblePerson,
            'nAtsiskaitytaPer' => $this->paymentDays,
            'sAtsiskaitymoTerminaiKred' => $this->paymentTermsCredit,
            'sAtsiskaitymoTerminaiDebt' => $this->paymentTermsDebit,
            'dDelspinigiaiKred' => $this->penaltyCredit,
            'dDelspinigiaiDebt' => $this->penaltyDebit,
            'nSkolaGalimaPradelstiDien' => $this->debtDaysAllowed,
            'nMaksPradlestosSkolosSum' => $this->maxOverdueDebt,
            'nKreditas' => $this->creditLimit,
            'sRusis' => $this->type,
            'nTipas' => $this->typeCode,
            'sPozymis1' => $this->tag1,
            'sPozymis2' => $this->tag2,
            'sPozymis3' => $this->tag3,
            'sObjektas1' => $this->object1,
            'sObjektas2' => $this->object2,
            'sObjektas3' => $this->object3,
            'sObjektas4' => $this->object4,
            'sObjektas5' => $this->object5,
            'sObjektas6' => $this->object6,
            'sPastabos' => $this->notes,
            'sPasiulomaValiuta' => $this->suggestedCurrency,
            'sYraKlientoPadalinys' => $this->hasSubsidiary === null ? null : (int) $this->hasSubsidiary,
            'dPapildomaInf' => $this->additionalInfo,
            'nFizinisAsmuo' => $this->isIndividual === null ? null : (int) $this->isIndividual,
            'nPvmMokaPirkejas' => $this->isVatPayer === null ? null : (int) $this->isVatPayer,
            'nSiustiFakturas' => $this->sendInvoices === null ? null : (int) $this->sendInvoices,
            'sDebtSask' => $this->debtAccount,
            'sKredSask' => $this->creditAccount,
            'nAktyvus' => $this->active === null ? null : (int) $this->active,
            'bNaujas' => $this->isNew === null ? null : (int) $this->isNew,
            'tKurimoData' => $this->createdAt,
            'tKoregavimoData' => $this->updatedAt,
        ], fn ($v) => $v !== null);
    }
}
